# Names, Source Files, Conventions, Operators, and So On

# Elixir identifiers must start with a letter or
# underscore, optionally followed by letters, digits, and
# underscores. Here letter means any UTF-8 letter
# character (optionally with a combining mark) and digit
# means a UTF-8 decimal-digit character. If you’re using
# ASCII, this does what you’d expect. The identifiers
# may end with a question mark or an exclamation mark.

# Here are some examples of valid variables:
# name    josé   _age   まつもと  _42  адрес!

# And some examples of invalid variables:
# name•   a±2  42
