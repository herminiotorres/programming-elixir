# booleans

# Truth

# Elixir has three special values related to Boolean
# operations: true, false, and nil. nil is treated as false in
# Boolean contexts.

# (A bit of trivia: all three of these values are aliases for
# atoms of the same name, so true is the same as the
# atom :true.)

# In most contexts, any value other than false or nil is
# treated as true. We sometimes refer to this as truthy as
# opposed to true.
